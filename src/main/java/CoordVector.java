public class CoordVector {
    Integer x=null;
    Integer y=null;
    public CoordVector(Integer x, Integer y){
        this.x=x;
        this.y=y;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }
}
